    static inline RGB aciToRgb(int index, bool isBlackBackground = true) {
        if (index < 0 || index > 255) {
            return {0, 0, 0}; // Default to black for invalid indices
        }

        uint32_t hex = ACI_TABLE[index];

        // Special handling for color 7 (White/Black)
        if (index == 7) {
            if (isBlackBackground) {
                hex = 0xFFFFFF; // White on black
            } else {
                hex = 0x000000; // Black on white
            }
        }

        return {
            static_cast<unsigned char>((hex >> 16) & 0xFF),
            static_cast<unsigned char>((hex >> 8) & 0xFF),
            static_cast<unsigned char>(hex & 0xFF)
        };
    }

